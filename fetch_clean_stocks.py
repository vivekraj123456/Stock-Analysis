def run_pipeline():
    print("\n" + "=" * 60)
    print("  STOCK PIPELINE STARTING (INDIA ONLY)")
    print(f"  Date range: {START_DATE} to {END_DATE}")
    print("=" * 60 + "\n")
    log.info("Pipeline started")

    all_frames = []
    failed = []

    for ticker in STOCKS:
        try:
            raw = fetch_stock_data(ticker)
            if raw.empty:
                failed.append(ticker)
                continue
            cleaned = clean_data(raw)
            enriched = add_features(cleaned)
            latest_dt = pd.to_datetime(enriched["Date"]).max().date()
            lag_days = (date.today() - latest_dt).days
            lag_note = f"  [stale {lag_days}d]" if lag_days > 7 else ""
            all_frames.append(enriched)
            print(f"  [OK] {ticker:<12}  {STOCKS[ticker]:<30}  {len(enriched):>6,} rows{lag_note}")
            if lag_days > 7:
                log.warning(f"{ticker}: latest data is stale by {lag_days} days (latest={latest_dt})")
        except Exception as e:
            log.error(f"Failed {ticker}: {e}")
            failed.append(ticker)
            print(f"  [ERR] {ticker}  ERROR: {e}")

    if not all_frames:
        print("[ERROR] No data fetched. Check internet connection.")
        return

    master = pd.concat(all_frames, ignore_index=True)
    master["Sector"] = master["Ticker"].map(SECTOR_MAP)

    export_for_powerbi(master)

    if failed:
        print(f"[WARN] Failed tickers: {failed}")

    log.info(f"Pipeline complete. {len(master):,} rows. Failed: {failed}")
if __name__ == "__main__":
    run_pipeline()



