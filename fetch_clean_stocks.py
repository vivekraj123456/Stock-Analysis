def build_date_table() -> pd.DataFrame:
    dates = pd.date_range(START_DATE, END_DATE, freq="D")
    dt = pd.DataFrame({"Date": dates})
    dt["Year"]          = dt["Date"].dt.year
    dt["Quarter"]       = "Q" + dt["Date"].dt.quarter.astype(str)
    dt["Month_Num"]     = dt["Date"].dt.month
    dt["Month_Name"]    = dt["Date"].dt.strftime("%B")
    dt["Month_Short"]   = dt["Date"].dt.strftime("%b")
    dt["Week"]          = dt["Date"].dt.isocalendar().week.astype(int)
    dt["Day_Name"]      = dt["Date"].dt.day_name()
    dt["Day_Num"]       = dt["Date"].dt.day
    dt["Is_Weekend"]    = dt["Date"].dt.dayofweek.isin([5,6]).astype(int)
    dt["Is_Business"]   = (~dt["Date"].dt.dayofweek.isin([5,6])).astype(int)
    dt["Year_Month"]    = dt["Date"].dt.strftime("%Y-%m")
    dt["Year_Quarter"]  = dt["Date"].dt.to_period("Q").astype(str)
    dt["Decade"]        = (dt["Year"] // 10 * 10).astype(str) + "s"
    return dt


